#include <unistd.h>

void	ft_is_negative(int n)
{
	char	c;
	char	a;

	c = 'P';
	a = 'N';
	if (n >= 0)
	{
		write(1, &c, 1);
	}
	else
	{
		write(1, &a, 1);
	}
}
